# for loop
# for loop is used to iterate a sequence (list, tuple, dictionary, set, or string)


# Example 1: using for loop with list
# l = [10, 2.5, "python", True]
# # to print this list values individually will use for loop
# for i in l:             # i can any character or _ also.
#     print(i)



# Directly using list in for loop
for i in [10, 2.5, "python", True]:             # i can any character or _ also.
    print(i)


# Example 2: using for loop with string
# s = "python"
# for ch in s:
#     print(ch)
# Directly using string in for loop
for ch in "python":
    print(ch)